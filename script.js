// Typing Animation
const texts = [
    'Full Stack Developer',
    'Mobile App Developer',
    'SEO Specialist',
    'Digital Marketing Expert',
    'UI/UX Designer'
];

let textIndex = 0;
let charIndex = 0;
let isDeleting = false;
const typingSpeed = 100;
const deletingSpeed = 50;
const pauseTime = 2000;

function typeText() {
    const currentText = texts[textIndex];
    const typingElement = document.querySelector('.typing-text');
    
    if (isDeleting) {
        typingElement.textContent = currentText.substring(0, charIndex - 1);
        charIndex--;
    } else {
        typingElement.textContent = currentText.substring(0, charIndex + 1);
        charIndex++;
    }
    
    let typeSpeed = isDeleting ? deletingSpeed : typingSpeed;
    
    if (!isDeleting && charIndex === currentText.length) {
        typeSpeed = pauseTime;
        isDeleting = true;
    } else if (isDeleting && charIndex === 0) {
        isDeleting = false;
        textIndex = (textIndex + 1) % texts.length;
    }
    
    setTimeout(typeText, typeSpeed);
}

// Start typing animation when page loads
document.addEventListener('DOMContentLoaded', () => {
    setTimeout(typeText, 1000);
});

// Profile Image Rotation
const profileImages = document.querySelectorAll('.profile-img');
let currentImageIndex = 0;

function rotateProfileImage() {
    profileImages[currentImageIndex].classList.remove('active');
    currentImageIndex = (currentImageIndex + 1) % profileImages.length;
    profileImages[currentImageIndex].classList.add('active');
}

// Rotate profile images every 4 seconds
setInterval(rotateProfileImage, 4000);

